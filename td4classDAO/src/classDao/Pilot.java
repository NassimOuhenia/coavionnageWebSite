package classDao;

public class Pilot {

	private String mail;
	private double experiences;

	public Pilot(String m) {
		mail = m;
		experiences = 0;
	}

	public String getMail() {
		return mail;
	}

	public void setMail(String m) {
		mail = m;
	}

	public double getExperiences() {
		return experiences;
	}

	public void setExperiences(String m) {
		mail = m;
	}

}
